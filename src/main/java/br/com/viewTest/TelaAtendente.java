        public void listar(){
            PacientesDAO dao = new PacientesDAO();
            List<Pacientes> lista = dao.listarPacientes();
            DefaultTableModel dados = (DefaultTableModel) tabelaPacientes.getModel();
            dados.setNumRows(0);
            
            for(Pacientes p: lista){
                dados.addRow(new Object[]{
                    p.getNome(),
                    p.getIdade(),
                    p.getProfissao(),
                    p.getEndereco(),
                    p.getVacinado()
                });
            }
    }
        
