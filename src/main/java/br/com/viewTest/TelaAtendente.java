    private void confirmaVacinacaoButtomActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmaVacinacaoButtomActionPerformed
        if (vacinadoCheckBox.isSelected() && confirmarTextField != null) {
            Pacientes p = new Pacientes();
            p.setNome(confirmarTextField.getText());

            PacientesDAO dao = new PacientesDAO();
            dao.confirmarVacinacao(p);           
            JOptionPane.showMessageDialog(null, "O Paciente " + confirmarTextField.getText() + " foi vacinado");
        } else {
            JOptionPane.showMessageDialog(null, "O Paciente " + confirmarTextField.getText() + " não esta cadastrado em nossa base");
        }
        

    }//GEN-LAST:event_confirmaVacinacaoButtomActionPerformed

